/* 
Dollars in einer Zukunft
*/

import java.io.*;

public class Solution {
    public static void main(String[] args) throws Exception {
        String n = ("$");
        int i = 0;
        while (i < 10){
            int b = 0;
            while (b < 10){
                System.out.print("$");
                b++;
            }
            System.out.println();
            i++;
        }

    }
}
